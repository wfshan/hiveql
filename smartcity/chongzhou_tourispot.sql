--3.是否留宿

select tf.age,tf.gender,tf.gw,max(tf.staynight) as staynight from
(select tt.uid,tt.age,tt.gender,tt.todate,tt.gw,sum(tt.staynight) as staynight from
(
select t.uid,t.age,t.gender,t.gw,to_date(t.etime) as todate,
case when t.stime<concat_ws(' ',to_date(t.etime),'6:00:00') and t.etime<concat_ws(' ',to_date(t.etime),'6:00:00')
then cast((unix_timestamp(t.etime)-unix_timestamp(t.stime)) as bigint)
when t.stime<concat_ws(' ',to_date(t.etime),'6:00:00') and t.etime>concat_ws(' ',to_date(t.etime),'6:00:00')
then cast((unix_timestamp(concat_ws(' ',to_date(t.etime),'6:00:00'))-unix_timestamp(t.stime)) as bigint)
else 0
end as staynight
from temp_wfs_chongzhou_20180903 t
where t.spot = 'anrenguzhen' and t.ptype = 0
group by t.uid,t.age,t.gender,t.gw,t.etime,t.stime
) tt
group by tt.uid,tt.age,tt.gender,tt.gw,tt.todate) tf
group by tf.uid,tf.age,tf.gender,tf.gw
;

